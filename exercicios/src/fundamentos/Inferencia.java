package fundamentos;

public class Inferencia {
	
	public static void main(String[] args) {
		
		double a = 2; // declaração e inicialização da variavel
		System.out.println(a);
		
		var b = 13.5;
		System.out.println(b);
				
		var c = "texto";
		System.out.println(c);
			/*
			 * a variavel c foi definida como string (texto) e n pode ser usada como numérico no Java.  (vice-versa)	
			 */
		
		double d; // declarei a variavel
		d = 126.65; // inicializei a variavel
		System.out.println(d); //executei a variavel
		}

}
